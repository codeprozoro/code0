// Primitive data types

// Primitive data types in programming languages are the most basic data types available and are built into the language. These data types are used to represent simple values.

// Here are some common primitive data types:

// Integer: A whole number without a decimal point (e.g. 10, -5, 0).

// Float: A number with a decimal point (e.g. 3.14, -2.5, 0.0).

// Boolean: A data type that can only take two values: true or false.

// Character: A single character (e.g. 'a', 'Z', '@').

// String: A sequence of characters (e.g. "Hello, World!", "12345").

// Null: A special value that represents the absence of a value.

// Different programming languages may have different primitive data types, but the ones listed above are very common in most languages.


